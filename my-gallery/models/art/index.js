///////////////////////////////////
// IMPORT SCHEMA AND MODEL
///////////////////////////////////
const { Schema, model } = require("mongoose");

////////////////////////////////////
// CREATE SCHEMA
////////////////////////////////////

const artSchema = new Schema({
  username: { type: String, required: true },
  title: String
});

////////////////////////////////////
// CREATE MODEL
////////////////////////////////////
const Art = model("art", artSchema);

////////////////////////////////////
// EXPORT MODEL
////////////////////////////////////
module.exports = Art;
